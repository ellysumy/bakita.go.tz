                        <h4>Huduma Zitolewazo na BAKITA</h4>
                        <div class="col-12 px-0 mt-4">

                            
    <strong><p>1.0&nbsp;&nbsp;Usambazaji wa Istilahi za Kiswahili</p></strong>

<p style="text-align:justify"><span style="font-size:12pt"><span style="font-family:&quot;Times New Roman&quot;,serif"><span style="font-family:&quot;Arial&quot;,sans-serif">Baraza hutoa ushauri kuhusu ukuzaji wa istilahi za Kiswahili na kujibu hoja za watumiaji wa Kiswahili zinazotafuta ufafanuzi kuhusiana na tafsiri sanifu za istilahi za nyanja mbalimbali.

Kuhimiza matumizi fasaha na kuzuia upotoshaji wa Kiswahili Baraza husikiliza na kupitia lugha katika vyombo vya habari kwa lengo la kuhimiza matumizi fasaha na kuzuia upotoshaji wake.

Huduma hii hufanyika kwa kupitia magazeti yaliyoandikwa kwa Kiswahili na kusikiliza matangazo ya redio na televisheni kwa lengo la kubainisha makosa na masahihisho yake. Matokeo ya kazi hiyo husambazwa kupitia mfululizo wa Makala za “Zingatia Matumizi Sahihi ya Kiswahili.”

</span><span style="font-family:&quot;Times New Roman&quot;,serif"><span style="font-family:&quot;Arial&quot;,sans-serif"></span></span></span></p>
                    






<strong><p>2.0&nbsp;&nbsp;Kuelimisha umma kuhusu makuzi na maendeleo ya Kiswahili kupitia vyombo vya habari.</p></strong>

<p style="text-align:justify"><span style="font-size:12pt"><span style="font-family:&quot;Times New Roman&quot;,serif"><span style="font-family:&quot;Arial&quot;,sans-serif">

  Huduma hiyo hutolewa kupitia vipindi vya redio na televisheni vinavyoandaliwa na kurushwa hewani na Baraza la Kiswahili la Taifa.

Vipindi hivyo ni kama vifuatavyo:-

Vipindi vya Redio"Lugha ya Taifa" kinachorushwa na TBC Taifa, siku ya Jumamosi saa 12:30 mpaka saa 1:00 jioni na"Kumepambazuka" kinachorushwa na Radio One, siku ya Jumamosi saa 1:30 mpaka saa 3:00 asubuhiVipindi vya Televisheni"Ulimwengu wa Kiswahili" kinachotangazwa na TBC1 siku ya Jumanne saa 7:30 mpaka 8:00 mchana na marudio yake ni siku ya Alhamisi saa 5:00 mpaka 5:30 Asubuhi.

</span><span style="font-family:&quot;Times New Roman&quot;,serif"><span style="font-family:&quot;Arial&quot;,sans-serif"></span></span></span></p>







<strong><p>3.0&nbsp;&nbsp;Ushauri kwa wanafunzi na watafiti.</p></strong>

<p style="text-align:justify"><span style="font-size:12pt"><span style="font-family:&quot;Times New Roman&quot;,serif"><span style="font-family:&quot;Arial&quot;,sans-serif">

  Baraza hutoa ushauri kwa wanafunzi wa Kiswahili wa ngazi zote waandikao miradi ya kielimu na tasnifu za vyuo vikuu kuhusu makuzi na maendeleo ya Kiswahili. Aidha Baraza hujibu hoja za watafiti na wachunguzi wa fani mbalimbali za Kiswahili.


</span><span style="font-family:&quot;Times New Roman&quot;,serif"><span style="font-family:&quot;Arial&quot;,sans-serif"></span></span></span></p>




<strong><p>4.0&nbsp;&nbsp;Huduma za ushauri wa uandishi wa Kiswahili kwa waandishi chipukizi</p></strong>

<p style="text-align:justify"><span style="font-size:12pt"><span style="font-family:&quot;Times New Roman&quot;,serif"><span style="font-family:&quot;Arial&quot;,sans-serif">

Baraza kwa maombi maalumu hupitia maandiko yaliyoandikwa na kufasiriwa katika Kiswahili kwa lengo la kuchunguza ufasaha wa lugha na hatimaye kuwapa ushauri.

</span><span style="font-family:&quot;Times New Roman&quot;,serif"><span style="font-family:&quot;Arial&quot;,sans-serif"></span></span></span></p>





<strong><p>5.0&nbsp;&nbsp;Huduma za Tafsiri na Ukalimani.</p></strong>

<p style="text-align:justify"><span style="font-size:12pt"><span style="font-family:&quot;Times New Roman&quot;,serif"><span style="font-family:&quot;Arial&quot;,sans-serif">

Baraza hutoa huduma ya tafsiri na ukalimani kwa serikali, mashirika ya umma na ya watu binafsi, na Asasi za kitaifa na kimataifa. Kuhusu tafsiri, Baraza lina wataalamu wa lugha mbalimbali zikiwemo Kiingereza, Kifaransa, Kijerumani, Kiarabu na Kireno.

Pale ambapo hakuna wataalamu wa baadhi ya lugha za kigeni kama vile Kichina, Kirusi, Kijpani, Kispania, n.k., Baraza limeainisha watalaamu wa nje wa lugha hizo ambao hufanya kazi za tafsiri baada ya kuidhinishwa na Baraza.

Kazi za tafsiri hutozwa kiasi fulani cha ada kwa ajili ya kufidia gharama zinazoambatana na ukamilishaji wa tafsiri. Kuhusu huduma ya ukalimani, Baraza lina wataalamu wake waliobobea katika kazi hiyo na pia limeorodhesha wakalimani wote nchini ambao upatikanaji wao huratibiwa na Baraza.
</span><span style="font-family:&quot;Times New Roman&quot;,serif"><span style="font-family:&quot;Arial&quot;,sans-serif"></span></span></span></p>



<strong><p>5.0&nbsp;&nbsp;Kutoa ithibati ya lugha</p></strong>

<p style="text-align:justify"><span style="font-size:12pt"><span style="font-family:&quot;Times New Roman&quot;,serif"><span style="font-family:&quot;Arial&quot;,sans-serif">

Kwa mujibu wa sheria ya kuanzishwa kwa Baraza ya mwaka 1967 pamoja na marekebisho yake ya mwaka 1983, kitabu chochote cha kiada au cha kutumika katika mfumo wa elimu nchini kilichoandikwa kwa Kiswahili hakina budi kupata ithibati ya lugha kutoka Baraza.

Kwa hiyo, mwandishi/ mchapishaji wa kitabu chochote kinachokusudiwa kutumika katika mfumo rasmi wa elimu anawajibika kukileta katika Baraza ambapo matumizi ya lugha hupitiwa. Baada ya Baraza kuhakikisha kuwa marekebisho yaliyopendekezwa yamefanywa, Baraza hutoa Cheti cha Ithibati ya Lugha ambacho huwasilishwa pamoja na kitabu Wizara ya Elimu na Mafunzo ya Ufundi kwa ajili ya kuhakiki maudhui ya kitabu. Huduma hii pia hutozwa kiasi cha ada.



</span><span style="font-family:&quot;Times New Roman&quot;,serif"><span style="font-family:&quot;Arial&quot;,sans-serif"></span></span></span></p>
                    





