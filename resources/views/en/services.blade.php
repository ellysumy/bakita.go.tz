<strong><p>5.0&nbsp;&nbsp;Provide language certification</p></strong>

<p style="text-align:justify"><span style="font-size:12pt"><span style="font-family:&quot;Times New Roman&quot;,serif"><span style="font-family:&quot;Arial&quot;,sans-serif">

According to the law establishing the Council in 1967 and its amendment in 1983, any textbook or book to be used in the education system in the country written in Kiswahili must be certified by the Council.

Therefore, the author/publisher of any book intended to be used in the official education system is responsible for bringing it to the Council where the use of language is reviewed. After the Council ensures that the proposed amendments have been made, the Council issues a Language Certification Certificate which is submitted together with the book to the Ministry of Education and Technical Training for review of the content of the book. This service is also subject to a fee.



