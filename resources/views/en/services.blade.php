<strong><p>5.0&nbsp;&nbsp;Translation and Interpretation Services</p></strong>

<p style="text-align:justify"><span style="font-size:12pt"><span style="font-family:&quot;Times New Roman&quot;,serif"><span style="font-family:&quot;Arial&quot;,sans-serif">

The Council provides translation and interpretation services to governments, public and private organizations, and national and international organizations. Regarding translation, the Council has experts in various languages ​​including English, French, German, Arabic and Portuguese.

Where there are no experts in some foreign languages ​​such as Chinese, Russian, Japanese, Spanish, etc., the Council has designated external experts in those languages ​​who do translation work after being approved by the Council.

Translation services are charged a certain amount of fee to cover the costs associated with the completion of the translation. Regarding the interpretation service, the Council has its own experts in the work and has also listed all the interpreters in the country whose availability is coordinated by the Council.
